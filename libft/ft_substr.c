#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*ss;
	size_t	slen;

	if (!s)
		return (NULL);
	slen = ft_strlen(s);
	if (start >= slen)
		return (ft_strdup(""));
	if (len > slen - start)
		len = slen - start;
	ss = (char *)malloc(len + 1);
	if (!ss)
		return (NULL);
	ft_strlcpy(ss, (const char *)s + start, len + 1);
	return (ss);
}
